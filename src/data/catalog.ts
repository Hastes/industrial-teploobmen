export type CatalogProductSpec = { label: string; value: string };

export type CatalogProductFeatureGroup = {
  title: string;
  items: string[];
};

export type CatalogDocument = {
  label: string;
  /** Путь от корня public, например "/assets/ПТО.pdf" */
  href: string;
  /** Подпись справа в кнопке: размер или формат */
  meta?: string;
};

export type CatalogProduct = {
  id: string;
  name: string;
  /** Например "Мультичастотная станция повышения давления" */
  series?: string;
  /** Например "AVEKTRA / Serval" */
  brand?: string;
  description: string;
  /** Маркетинговая строка-обещание */
  tagline?: string;
  specs: CatalogProductSpec[];
  /** Сгруппированные характеристики для расширенного блока */
  features?: CatalogProductFeatureGroup[];
  /** Партнёрские компоненты в составе */
  components?: string[];
  /** Нормативы и сертификаты */
  compliance?: string[];
  /** PDF-каталоги/опросники по конкретному продукту */
  documents?: CatalogDocument[];
};

export type CatalogProjectType = {
  id: string;
  name: string;
  description: string;
};

export type CatalogCategory = {
  id: string;
  number: string;
  eyebrow: string;
  title: string;
  description: string;
  highlights: string[];
  productsLabel: string;
  products: CatalogProduct[];
  /** Дополнительные направления под индивидуальный подбор. */
  typesLabel?: string;
  types?: CatalogProjectType[];
  cta: { label: string; href: string };
  image: { src: string; alt: string; width: number; height: number };
};

